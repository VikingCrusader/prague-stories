/**
 * Exports all preset locations from the live DB and rewrites three files:
 *   - pragueLocations.js        (static seed array — replaces all batches)
 *   - seedDescriptionsStatic.js (all descriptions — rebuilt from DB)
 *   - seedLocalizedNames.js     (all localizedNames — rebuilt from DB)
 *
 * Run: node src/data/exportLocations.js
 */

import 'dotenv/config';
import { writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import path from 'path';
import { connectDB } from '../config/db.js';
import Location from '../models/Location.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

function esc(str) {
  return (str || '').replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\${/g, '\\${');
}

async function run() {
  await connectDB();

  const locations = await Location.find({ isPreset: true })
    .sort({ createdAt: 1 })
    .lean();

  console.log(`Found ${locations.length} preset locations.`);

  // ── 1. pragueLocations.js ───────────────────────────────────────────────────
  const locLines = locations.map(loc => {
    const parts = [
      `    name: ${JSON.stringify(loc.name)}`,
      `    slug: ${JSON.stringify(loc.slug)}`,
      `    category: ${JSON.stringify(loc.category)}`,
      `    coordinates: { lat: ${loc.coordinates.lat}, lng: ${loc.coordinates.lng} }`,
      `    wikipediaUrl: ${JSON.stringify(loc.wikipediaUrl || '')}`,
      `    pixelArtKey: ${JSON.stringify(loc.pixelArtKey || '')}`,
      `    xpReward: ${loc.xpReward}, difficulty: ${loc.difficulty}`,
    ];
    if (loc.localizedNames?.cz || loc.localizedNames?.zh) {
      parts.push(`    localizedNames: { cz: ${JSON.stringify(loc.localizedNames.cz || '')}, zh: ${JSON.stringify(loc.localizedNames.zh || '')} }`);
    }
    return `  {\n${parts.join(',\n')},\n  }`;
  });

  const pragueLocationsContent =
    `// Auto-generated by exportLocations.js — do not edit manually\n` +
    `export const pragueLocations = [\n${locLines.join(',\n')},\n];\n`;

  writeFileSync(path.join(__dirname, 'pragueLocations.js'), pragueLocationsContent, 'utf8');
  console.log('✓ pragueLocations.js rewritten');

  // ── 2. seedDescriptionsStatic.js ────────────────────────────────────────────
  const descEntries = locations
    .filter(loc => loc.description?.en || loc.description?.cz || loc.description?.zh)
    .map(loc => {
      const en = esc(loc.description?.en || '');
      const cz = esc(loc.description?.cz || '');
      const zh = esc(loc.description?.zh || '');
      return `  ${JSON.stringify(loc.slug)}: {\n    en: \`${en}\`,\n    cz: \`${cz}\`,\n    zh: \`${zh}\`,\n  }`;
    });

  const seedDescContent =
    `import 'dotenv/config';\n` +
    `import { connectDB } from '../config/db.js';\n` +
    `import Location from '../models/Location.js';\n\n` +
    `// Auto-generated by exportLocations.js — do not edit manually\n` +
    `const descriptions = {\n${descEntries.join(',\n\n')},\n};\n\n` +
    `async function seedDescriptionsStatic() {\n` +
    `  await connectDB();\n` +
    `  const slugs = Object.keys(descriptions);\n` +
    `  console.log(\`Saving descriptions for \${slugs.length} locations...\\n\`);\n` +
    `  let done = 0, failed = 0;\n` +
    `  for (const slug of slugs) {\n` +
    `    process.stdout.write(\`Saving "\${slug}"... \`);\n` +
    `    try {\n` +
    `      const result = await Location.findOneAndUpdate(\n` +
    `        { slug },\n` +
    `        { $set: { description: descriptions[slug] } },\n` +
    `        { new: true }\n` +
    `      );\n` +
    `      if (!result) { failed++; console.log('FAILED — not found'); }\n` +
    `      else { done++; console.log(\`done (\${done}/\${slugs.length})\`); }\n` +
    `    } catch (err) { failed++; console.log(\`FAILED — \${err.message}\`); }\n` +
    `  }\n` +
    `  console.log(\`\\nFinished. \${done} saved, \${failed} failed.\`);\n` +
    `  process.exit(failed > 0 ? 1 : 0);\n` +
    `}\n\n` +
    `seedDescriptionsStatic().catch(err => { console.error(err); process.exit(1); });\n`;

  writeFileSync(path.join(__dirname, 'seedDescriptionsStatic.js'), seedDescContent, 'utf8');
  console.log('✓ seedDescriptionsStatic.js rewritten');

  // ── 3. seedLocalizedNames.js ─────────────────────────────────────────────────
  const nameEntries = locations
    .filter(loc => loc.localizedNames?.cz || loc.localizedNames?.zh)
    .map(loc =>
      `  { slug: ${JSON.stringify(loc.slug)}, cz: ${JSON.stringify(loc.localizedNames.cz || '')}, zh: ${JSON.stringify(loc.localizedNames.zh || '')} }`
    );

  const seedNamesContent =
    `import 'dotenv/config';\n` +
    `import { connectDB } from '../config/db.js';\n` +
    `import Location from '../models/Location.js';\n\n` +
    `// Auto-generated by exportLocations.js — do not edit manually\n` +
    `const localizedNames = [\n${nameEntries.join(',\n')},\n];\n\n` +
    `async function run() {\n` +
    `  await connectDB();\n` +
    `  let done = 0;\n` +
    `  for (const { slug, cz, zh } of localizedNames) {\n` +
    `    const result = await Location.findOneAndUpdate(\n` +
    `      { slug },\n` +
    `      { $set: { 'localizedNames.cz': cz, 'localizedNames.zh': zh } },\n` +
    `      { new: true }\n` +
    `    );\n` +
    `    if (result) { done++; console.log(\`Updated: \${slug}\`); }\n` +
    `    else console.log(\`Not found: \${slug}\`);\n` +
    `  }\n` +
    `  console.log(\`\\nDone. \${done} updated.\`);\n` +
    `  process.exit(0);\n` +
    `}\n\n` +
    `run().catch(err => { console.error(err); process.exit(1); });\n`;

  writeFileSync(path.join(__dirname, 'seedLocalizedNames.js'), seedNamesContent, 'utf8');
  console.log('✓ seedLocalizedNames.js rewritten');

  console.log('\nAll scripts synced to current DB state.');
  process.exit(0);
}

run().catch(err => { console.error(err); process.exit(1); });
